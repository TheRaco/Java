public class Punto {
float x;
float y;
Punto (float x, float y) {
    this.x=x;
    this.y=y;
}
Punto (){
    x=0;
    y=0;
}
public void setX (float x){
    this.x=x;
}
public void setY(float y){
    this.y=y;
}
public float getX(){
    return x;
}
public float getY(){
return y;    
  }
}
